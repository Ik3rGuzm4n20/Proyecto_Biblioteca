    void listar(){
        DefaultTableModel modelo=new DefaultTableModel();
        Connection conectar=null;
        cadena=tfbuscar.getText();
        CadenaCompleta="select * from libros where libros.CodCategoria='"+tfbuscar.getText()+"'";
             
        try{
                conet=con1.getConnection();
                st = conet.createStatement();
                rs= st.executeQuery(CadenaCompleta);
                modelo.setColumnIdentifiers(new Object[]{"Codigo","Nombre","CodProveedor","CodCategoria","CodAutor","Precio"});
                
                while(rs.next()){
                    modelo.addRow(new Object[]{
                    rs.getInt("Codigo"),
                    rs.getString("Nombre"),
                    rs.getString("CodProveedor"),
                    rs.getString("CodCategoria"),
                    rs.getString("CodAutor"),
                    rs.getString("Precio")});
                    tfp.setText("1");
                }
                TablaC.setModel(modelo); 
                if (tfp.getText().equals("1")){
                    JOptionPane.showMessageDialog(null,"RESULTADOS");
                    tfp.setText("");
                    }
                    else{
                    JOptionPane.showMessageDialog(null,"ERROR: Categoria NO Encontrada");
                    tfp.setText("");
                    }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
    }
    

