    private void TablaCMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaCMouseClicked
        // TODO add your handling code here:
        int fila=TablaC.getSelectedRow();
        if (fila == -1){
        
        JOptionPane.showMessageDialog(null, "No Se Seleccionó Fila");
        }else{
            String Codigo= (String) TablaC.getValueAt (fila, 0).toString();
            String Nombre= (String) TablaC.getValueAt (fila, 1);
            String Proveedor= (String) TablaC.getValueAt (fila, 2);
            String Categoria= (String) TablaC.getValueAt (fila, 3);
            String Autor= (String) TablaC.getValueAt (fila, 4);
            String Precio= (String) TablaC.getValueAt (fila, 5);
            
            tfcodigo.setText(Codigo);
            tfnombre.setText(Nombre);
            tfproveedor.setText(Proveedor);
            tfcategoria.setText(Categoria);
            tfautor.setText(Autor);
            tfprecio.setText(Precio);

        }
    }//GEN-LAST:event_TablaCMouseClicked

