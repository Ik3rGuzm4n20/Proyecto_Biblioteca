    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        // TODO add your handling code here:
        int fila=Tabla.getSelectedRow();
        if (fila == -1){

            JOptionPane.showMessageDialog(null, "No Se Seleccionó Fila");
        }else{
            String Codigo= (String) Tabla.getValueAt (fila, 0).toString();
            String Nombre= (String) Tabla.getValueAt (fila, 1);
            String Nit= (String) Tabla.getValueAt (fila, 2);
            String Telefono= (String) Tabla.getValueAt (fila, 3);
            String Direccion= (String) Tabla.getValueAt (fila, 4);

            tfcodigo.setText(Codigo);
            tfnombre.setText(Nombre);
            tfnit.setText(Nit);
            tftelefono.setText(Telefono);
            tfdireccion.setText(Direccion);
        }
    }//GEN-LAST:event_TablaMouseClicked

