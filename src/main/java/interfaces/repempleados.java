     void consultar(){
            String sql ="Select * from empleados";
    
            try{
                conet=con1.getConnection();
                st = conet.createStatement();
                rs= st.executeQuery(sql);
                Object[] empleado=new Object[6];
                modelo=(DefaultTableModel) Tabla.getModel();
                while (rs.next()){
                    empleado [0]=rs.getInt("Codigo");
                    empleado [1]=rs.getString("Nombre");
                    empleado [2]=rs.getString("Nit");
                    empleado [3]=rs.getString("Telefono");
                    empleado [4]=rs.getString("Direccion");
                    
                    modelo.addRow(empleado);
                }
                Tabla.setModel(modelo);
                
                } catch (SQLException ex) {
                }
    }
    
    void modificar(){
    try{
        if (tfcodigo.getText().equals("")||tfnombre.getText().equals("")||tfnit.getText().equals("")||tftelefono.getText().equals("")||tfdireccion.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"ERROR: Campos Vacios");
        } else {
            String sql="Update empleados set Codigo='"+tfcodigo.getText()+"', Nombre='"+tfnombre.getText()+"', Nit='"+tfnit.getText()+"', Telefono='"+tftelefono.getText()+"', Direccion='"+tfdireccion.getText()+"' where Codigo="+tfcodigo.getText();
            conet=con1.getConnection();
            st=conet.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Datos del Empleado Modificados");
            limpiarTabla();
        }
        } catch (SQLException ex) {
        }
    }
    
    void eliminar(){
        int fila=Tabla.getSelectedRow();
        try {
             if(fila<0){
            JOptionPane.showMessageDialog(null,"Empleado No Seleccionado");
            limpiarTabla();
             }
             else{
                 String sql="delete from empleados where Codigo="+tfcodigo.getText();
                 conet=con1.getConnection();
                 st=conet.createStatement();
                 st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Empleado ELIMINADO");
                limpiarTabla();
             }   
            } catch (SQLException ex) {
            }
    }
          
