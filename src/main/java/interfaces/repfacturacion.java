    void consultar(){
            String sql ="Select * from facturas";
    
            try{
                conet=con1.getConnection();
                st = conet.createStatement();
                rs= st.executeQuery(sql);
                Object[] facturas=new Object[4];
                modelo=(DefaultTableModel) Tabla.getModel();
                while (rs.next()){
                    facturas [0]=rs.getInt("idfacturas");
                    facturas [1]=rs.getString("CodLibros");
                    facturas [2]=rs.getString("CodClientes");
                    facturas [3]=rs.getString("CodEmpleados");
                    
                    modelo.addRow(facturas);
                }
                Tabla.setModel(modelo);
                
                } catch (SQLException ex) {
                }
    }
    
    void modificar(){
    try{
        if (tfid.getText().equals("")||tflibro.getText().equals("")||tfcliente.getText().equals("")||tfempleado.getText().equals("")) {
            JOptionPane.showMessageDialog(null,"ERROR: Campos Vacios");
        } else {
            String sql="Update facturas set idfacturas='"+tfid.getText()+"', CodLibros='"+tflibro.getText()+"', CodClientes='"+tfcliente.getText()+"', CodEmpleados='"+tfempleado.getText()+"' where idfacturas="+tfid.getText();
            conet=con1.getConnection();
            st=conet.createStatement();
            st.executeUpdate(sql);
            JOptionPane.showMessageDialog(null,"Datos de la Factura Modificados");
            limpiarTabla();
        }
        } catch (SQLException ex) {
        }
    }
    
    void eliminar(){
        int fila=Tabla.getSelectedRow();
        try {
             if(fila<0){
            JOptionPane.showMessageDialog(null,"Factura No Seleccionada");
            limpiarTabla();
             }
             else{
                 String sql="delete from facturas where idfacturas="+tfid.getText();
                 conet=con1.getConnection();
                 st=conet.createStatement();
                 st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null,"Factura ELIMINADA");
                limpiarTabla();
             }   
            } catch (SQLException ex) {
            }
    }
          
    void nuevo(){
            tfid.setText("");
            tflibro.setText("");
            tfcliente.setText("");
            tfempleado.setText("");
            tfid.requestFocus();
    }
    
    void limpiarTabla(){
            for (int i=0; i<Tabla.getRowCount();i++){
            modelo.removeRow(i);
            i=i-1;
            }
    }
    
